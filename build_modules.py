GOOD_FONTS = {'JournalSansC-Bold', 'JournalSansC', 'JournalSansC-Italic',
              'JournalSerif', 'JournalSerif-Bold', 'JournalSerif-Italic',
              'TimesNewRoman', 'TimesNewRoman-Bold', 'Arial', 'Arial-Bold'}

def fix_text(text, font_name):
    if not text or not text.strip(): return text
    if font_name in GOOD_FONTS: return text
    if any('А' <= c <= 'я' or c in 'Ёё№' for c in text): return text
    try: return text.encode('latin-1').decode('cp1251')
    except: return text

def get_page_text(page):
    blocks = page.get_text('dict')['blocks']
    lines = []
    for b in blocks:
        if 'lines' not in b: continue
        for l in b['lines']:
            line_text = ''
            for s in l['spans']:
                line_text += fix_text(s['text'], s['font'])
            txt = line_text.strip()
            if txt: lines.append(txt)
    return '\n'.join(lines)

def extract_chapter_text(start_page, end_page):
    pages_text = []
    for pg in range(start_page, min(end_page + 1, len(doc))):
        text = get_page_text(doc[pg])
        if text.strip(): pages_text.append(text)
    full = '\n\n'.join(pages_text)
    full = re.sub(r'-\s*\d+\s*-', '', full)
    full = re.sub(r'\n{3,}', '\n\n', full)
    return full

def text_to_html(text, max_chars=3000):
    paragraphs = text.strip().split('\n\n')
    html_parts = []
    char_count = 0
    for p in paragraphs:
        p = p.strip().replace('\n', ' ')
        if len(p) > 20:
            html_parts.append(f'<p>{p}</p>')
            char_count += len(p)
            if char_count >= max_chars:
                break
    return '\n'.join(html_parts) if html_parts else '<p>[Текст извлекается]</p>'

